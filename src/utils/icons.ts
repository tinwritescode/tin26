/**
 * Get a lucide-react icon component by name
 * @param iconName - The name of the icon (e.g., "CheckCircle")
 * @returns The icon component or a default icon if not found
 */
export function getIcon(iconName: string): LucideIcon {
  return iconMap[iconName] || CheckCircle
}

/**
 * Check if an icon name is valid
 * @param iconName - The name of the icon to check
 * @returns True if the icon exists in the map
 */
export function isValidIcon(iconName: string): boolean {
  return iconName in iconMap
}
